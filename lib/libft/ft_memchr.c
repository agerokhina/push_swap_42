#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*str;
	unsigned char	chr;

	str = (unsigned char *)s;
	chr = (unsigned char)c;
	while (n > 0)
	{
		if (str[0] == chr)
			return ((void *)str);
		str++;
		n--;
	}
	return (NULL);
}
